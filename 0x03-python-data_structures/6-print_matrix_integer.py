#!/usr/bin/python3
def print_matrix_integer(matrix=[[]]):
    mtx1 = matrix[0]
    for i in mtx1:
        print("{:d}".format(i), end=' ')
    print()
    mtx2 = matrix[1]
    for i in mtx2:
        print("{:d}".format(i), end=' ')
    print()
    mtx3 = matrix[2]
    for i in mtx3:
        print("{:d}".format(i), end=' ')
    print()
